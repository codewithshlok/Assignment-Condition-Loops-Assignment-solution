// Q5. Use the while loop to print numbers from 1 to 25 in ascending and descending order.

// for ascending Order
console.log("|Ascending Order|")
let i = 0;
while(i <= 25){
    console.log(i)
    i++;
}

console.log("|Descending Order|")

// Printing numbers from 25 to 1 in descending order using a while loop
let n = 25;
while (n >= 1) {
    console.log(n);
    n--;
}

